// Interaktion: Klick aktiviert einen Channel, ohne Interaktion wandert die
// Hervorhebung automatisch weiter (pausiert nach Klick, respektiert
// prefers-reduced-motion). Gibt eine Cleanup-Funktion zurück.
export function initChannelsVisual() {
  const root = document.getElementById('channels-visual');
  if (!root) return null;

  const nodes = [...root.querySelectorAll('.channel-node')];
  const lines = [...root.querySelectorAll('.channel-line')];
  const infoTitle = document.getElementById('channels-info-title');
  const infoText = document.getElementById('channels-info-text');
  const keys = nodes.map((n) => n.dataset.key);
  let index = 0;
  let cycleTimer = null;
  let resumeTimer = null;

  function activate(key) {
    index = keys.indexOf(key);
    nodes.forEach((n) => {
      const active = n.dataset.key === key;
      n.classList.toggle('active', active);
      n.setAttribute('aria-pressed', String(active));
    });
    lines.forEach((l) => l.classList.toggle('active', l.dataset.key === key));
    infoTitle.textContent = t(`channels.nodes.${key}.label`);
    infoText.textContent = t(`channels.nodes.${key}.text`);
  }

  const prefersReduced = window.matchMedia('(prefers-reduced-motion: reduce)').matches;

  function startCycle() {
    if (prefersReduced) return;
    cycleTimer = setInterval(() => activate(keys[(index + 1) % keys.length]), 3500);
  }

  nodes.forEach((n) => {
    n.addEventListener('click', () => {
      activate(n.dataset.key);
      clearInterval(cycleTimer);
      clearTimeout(resumeTimer);
      resumeTimer = setTimeout(startCycle, 12000);
    });
  });

  activate(keys[0]);
  startCycle();

  return () => {
    clearInterval(cycleTimer);
    clearTimeout(resumeTimer);
  };
}
