const RADIUS = 41; // Kreisradius in % des Containers

function nodePosition(index) {
  const angle = (-90 + index * (360 / CHANNEL_NODES.length)) * (Math.PI / 180);
  return {
    x: +(50 + RADIUS * Math.cos(angle)).toFixed(2),
    y: +(50 + RADIUS * Math.sin(angle)).toFixed(2),
  };
}

